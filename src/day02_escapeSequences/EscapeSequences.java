package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello students\nToday we have Java class");

        System.out.println("----------------------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\t Java Developer Course Batch 1");

        System.out.println("----------------------------------------");

        System.out.println("\\");

        System.out.println("----------------------------------------");

        System.out.println("My favorite TV show is \"The Walking Dead\"");
    }


}



/*

  1. \n: New line
  2. \t: paragraph space
  3. \\: Backs lash
  4. \": doule quote

  Multi line comments
 */
