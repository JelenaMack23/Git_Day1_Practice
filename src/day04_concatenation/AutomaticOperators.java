package day04_concatenation;

public class AutomaticOperators {

    public static void main(String[] args) {

        System.out.println( 100 + 200);

        System.out.println(40 - 10);

        System.out.println(50 / 6);

        System.out.println(50d / 6);

        System.out.println( (int) (2.5 / 0.5) );

      //  System.out.println( 9 / 0); dominator can not be 0 zero

        System.out.println( 10 * 2);

        System.out.println( 100 % 10 );

        System.out.println(45 % 5);





    }
}

